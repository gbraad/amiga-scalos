<p>If you're wondering how to make your Scalos desktop experience that little bit better, you're in the right place. We've put together a number of hints, tips and ideas that will help you do just that.<br><br>
Please note that some of these tips may require additional programs. They are highlighted where possible.</p>

<br><hr size="1" width="90%"><br>

<p>If you're using an 8-bit screen mode, you can enhance image quality by installing the <a href="products/scalos/hints/ams.lha">V42 picture.datatype</a> by David Junod. For 16/24-bit CyberGraphX 
screen modes, it's advisable to use the <a href="products/scalos/hints/PictDT43.lha">V43 picture.datatype</a>. Better still, use the picture.datatype from AmigaOS 3.5+!</p>

